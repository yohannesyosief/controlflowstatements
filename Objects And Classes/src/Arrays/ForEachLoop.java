package Arrays;
//We can also print java Array using for-each loop.The java for-each loop prints the array elements one by one
//It holds an array element in a variable,then executes the body of the loop.
//Now let's ee a java program to print the array elements using for-each loop
public class ForEachLoop {

	public static void main(String[] args) {
		int arr[]= {33,4,50,7};
		//pritnting array using for each loop
		for(int i:arr) {
			System.out.println(i);
		}
	}

}
