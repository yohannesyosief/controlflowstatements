package OperatorsInJava;
//Java ternary operator is usedas one liner replacement for if-then-else statement and used a lot in Java programming.
//It is the only conditional operator which takes three operators

public class JavaTernaryOperator {

	public static void main(String[] args) {
		int a=2;
		int b=5;
		int c=10;
		
		int min=(a<b)?a:b;
		System.out.println(min);
		int min2=(a>c)?a:c;
		System.out.println(min2);
		}}
