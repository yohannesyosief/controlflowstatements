package OperatorsInJava;
//Java assignment operator is one of the most common operator.It is used to assign the value on its right to the operand on it left.

public class JavaAssignmentOperator {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		a+=4;//a=a+4(a=10+4)
		b-=4;//b=b-4(20-4)
		System.out.println(a);
		System.out.println(b);
		}}
